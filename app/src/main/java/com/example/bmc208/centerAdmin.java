package com.example.bmc208;

public class centerAdmin {
    String center, addressAdmin;

    public centerAdmin(String center, String addressAdmin) {
        this.center = center;
        this.addressAdmin = addressAdmin;
    }

    public String getCenter() {
        return center;
    }

    public void setCenter(String center) {
        this.center = center;
    }

    public String getAddressAdmin() {
        return addressAdmin;
    }

    public void setAddressAdmin(String addressAdmin) {
        this.addressAdmin = addressAdmin;
    }

}
